# for循环基本用法
pizza_names = ['A', 'B', 'C']
for pizza_name in pizza_names:
    print("I like "+pizza_name.title()+" Pizza.\n")
print("I really like C Pizza.")
ani_names = ['dog', 'cat', 'duck']
for ain_name in ani_names:
    print("A "+ain_name.title()+" would make a pet.")
print("Any of these animals would make a great pet!")

